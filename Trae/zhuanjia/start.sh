#!/bin/bash

# 专家数字人平台启动脚本
# 一键启动开发服务器，方便演示使用

echo "=========================================="
echo "专家数字人平台 - 快速启动"
echo "=========================================="

# 切换到项目目录
cd "$(dirname "$0")"

# 检查是否已安装依赖
if [ ! -d "node_modules" ]; then
    echo "检测到未安装依赖，正在安装..."
    npm install
    echo "依赖安装完成！"
fi

# 启动开发服务器
echo "正在启动开发服务器..."
echo "服务器启动后，请访问: http://localhost:5173/"
echo "按 Ctrl+C 可停止服务器"
echo "=========================================="

npm run dev